$(function(){
    $('[data="cover"]').hover(
      function() {
         $(this).children('.open-cover').removeClass( "fn-hide" );
      }, function() {
        $(this).children('.open-cover').addClass( "fn-hide" );
      }
    );
    
    $('#gallery a').lightBox({fixedNavigation:true});
    
    $('#galleryBrowse a').lightBox({fixedNavigation:true});
    
    $('#dialog').jqm({
        trigger: '#videoModal'
    });
    
    $('#dialogMap').jqm({
        trigger:'#mapModal'
    });
})
